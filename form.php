                }
            });

            $(document.body).on("change", "#OtherSizeText", function () {
                if (this.value != '') {
                    $(".SizeValueVali").hide();
                }
                else {
                    $(".SizeValueVali").show();
                }
            });

            $(document.body).on("change", "#OtherSizeLength", function () {
                if (this.value != '') {
                    $(".LengthValueVali").hide();
                }
                else {
                    $(".LengthValueVali").show();
                }
            });

            $(document.body).on("click", ".btn_chose", function () {
                $("#SizeValue").val();
                $("#LengthValue").val();
                if ($("#SizeValue").val() == "other" && $("#OtherSizeText").val() == '') {
                    $(".SizeValueVali").show();
                }

                if ($("#LengthValue").val() == "other" && $("#OtherLengthText").val() == '') {
                    $(".LengthValueVali").show();
                }

                if ($("#KgValue").val() == '') {
                    $(".KgValueVali").show();
                }
                else {

                    if ($("#SizeValue").val() == "other" && $("#OtherSizeText").val() == '') {
                        $(".SizeValueVali").show();
                    }
                    else if ($("#LengthValue").val() == "other" && $("#OtherLengthText").val() == '') {
                        $(".LengthValueVali").show();
                    }
                    else {
                        var size = "";
                        var length = "";
                        if ($("#SizeValue").val() == "other" || $("#OtherSizeText").val() != '') {
                            size = $("#OtherSizeText").val();
                        }
                        else {
                            size = $("#SizeValue").val();
                        }

                        if ($("#LengthValue").val() == "other" || $("#OtherLengthText").val() != '') {
                            length = $("#OtherLengthText").val();
                        }
                        else {
                            length = $("#LengthValue").val();
                        }

                        var tr = "<tr><td>" + size + "</td><td>" + length + "</td><td>" + $("#KgValue").val() + "</td><td><i class='icofont-trash removetr'></i></td></tr>";
                        $(".btn_selectSizeLengthTableBody").append(tr);
                        $(".selectSizeLengthTable").show();

                        $("#myModal").modal("hide");
                    }

                }
            });

